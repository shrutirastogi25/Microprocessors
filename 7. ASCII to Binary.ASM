.MODEL SMALL

.DATA
inputMsg db "ENTER ANY ASCII CHARACTER: $"
OUTPUTmSG DB "binary equivalent is: $"

.CODE
MOV AX,@data
MOV DS,AX
MOV DX,offset inputMsg
MOV AH,09H
INT 21H

;READ A CHARACTER
MOV AH,01H
INT 21H

MOV BL,AL

MOV AH,09H
LEA DX,outputMsg
INT 21H

MOV CX,8
BIN_CONV:
SHL BL,1
JC DISPLAY1

DISPLAY0:
MOV DL,'0'
JMP DISP

DISPLAY1:
MOV DL, '1'

DISP:
MOV AH,02H
INT 21H

LOOP BIN_CONV
MOV AH,4CH
INT 21H
END

