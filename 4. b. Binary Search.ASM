.MODEL SMALL
.386
.DATA
ARRAY DB 15 DUP(?)
MSG1 DB 13,10,'ENTER THE NUMBER:$'
MSG2 DB 13,10,'ENTER THE NUMBER TO BE SEARCHED:$'
MSG3 DB 13,10,'NUMBER FOUND$'
MSG4 DB 13,10,'NUMBER NOT FOUND$'
MSG5 DB 13,10,'ENTER THE NUMBER OF ELEMENTS:$'
UL DB ?
LL DB 0H
K DB ?

.CODE
MOV AX,0
MOV AX,@data
MOV DS,AX
MOV AX,0
MOV AH,09H
MOV DX,OFFSET MSG5
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H
MOV CL,AL
MOV UL,AL

MOV SI,OFFSET ARRAY

PARA1:
        MOV AH,09H
        MOV DX,OFFSET MSG1
        INT 21H
        MOV AH,01H
        INT 21H
        SUB AL,30H
        MOV ARRAY[SI],AL
        INC SI
        LOOP PARA1

MOV AH,09H
MOV DX,OFFSET MSG2
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H
MOV K,AL

SEARCH:
        MOV BL,UL
        MOV DL,LL
        SUB BL,DL
        JC OUTPUT1
        MOV BL,UL
        MOV AL,BL
        ADD AL,DL
        MOV CL,02H
        DIV CL
        MOV AH,00H
        MOV BX,AX
        MOV AL,ARRAY[BX]
        CMP K,AL
        JZ OUTPUT2
        JL LOOP1
        JG LOOP2

LOOP1:
        SUB BL,01H
        MOV UL,BL
        JMP SEARCH

LOOP2:
        ADD BL,01H
        MOV LL,BL
        JMP SEARCH

OUTPUT1:
        MOV AH,09H
        MOV DX,OFFSET MSG4
        INT 21H
        JMP PARA2

OUTPUT2:
        MOV AH,09H
        MOV DX,OFFSET MSG3
        INT 21H

PARA2:
        MOV AH,4CH
        INT 21H

END
