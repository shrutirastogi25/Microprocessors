.MODEL SMALL

.DATA
ARRAY DB 20 DUP (?)
SZ DB 00H

MSG1 DB 10,13,"ENTER THE SIZE OF THE ARRAY:$"
MSG2 DB 10,13,"ENTER THE ARRAY:$"
MSG3 DB 10,13,"SORTED ARRAY: $"

.CODE
MOV AX,@data
MOV DS,AX
MOV DX,OFFSET MSG1
INT 21H

MOV AH,01
INT 21H
SUB AL,30H
MOV CL,AL

MOV SZ,AL
MOV AH,09
MOV DX,OFFSET MSG2
INT 21H

MOV SI,0
MOV CL,SZ

L1:
	MOV DL,20H
	MOV AH,02H
	INT 21H
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV ARRAY[SI],AL
	INC SI
	LOOP L1

MOV CL,SZ
MOV CH,0
MOV DI,CX

L2:
	MOV SI,0
	MOV CL,SZ

L3:
	MOV AL,ARRAY[SI]
	CMP AL,ARRAY[SI+1]
	JL L4
	XCHG AL,ARRAY[SI+1]
	MOV ARRAY[SI],AL

L4:
	INC SI
	LOOP L3
	DEC DI
	JNZ L2

MOV DL,0AH
MOV AH,02H
INT 21H

MOV CL,SZ
MOV SI,1
MOV AH,09
MOV DX,OFFSET MSG3
INT 21H

L5:
	MOV DL,20H
	MOV AH,02H
	INT 21H
	MOV DL,ARRAY[SI]
	INC SI
	ADD DL,30H
	MOV AH,02H
	INT 21H
	LOOP L5

MOV AH,4CH
INT 21H
END