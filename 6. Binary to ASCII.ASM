.model small
.stack
.data
ARRAY DB 8 DUP(?)
N1 DB 00H
N2 DB 00H

INPUTMSG DB 0DH,0AH,"ENTER BINARY NUMBER:$"
OUTPUTMSG DB 0DH,0AH,"ENTERED CHARACTER IS: $"

.CODE
start:
MOV AX,@DATA
MOV DS,AX
MOV AH,09H
MOV DX,OFFSET INPUTMSG
INT 21H

MOV CX,8
MOV SI,0

READ:
        MOV AH,01H
        INT 21H
        SUB AL,30H
        MOV ARRAY[SI],AL
        INC SI
        LOOP READ

MOV N1,1
MOV CX,8
MOV BL,2
MOV DL,0
MOV SI,7

ASCII_EQ:
        MOV AL,ARRAY[SI]
        MUL N1
        ADD DL,AL
        MOV AL,N1
        MUL BL
        MOV N1,AL
        DEC SI
        LOOP ASCII_EQ

MOV N2,DL
MOV AX,@DATA
MOV DS,AX
MOV AH,09H
MOV DX,OFFSET OUTPUTMSG
INT 21H
MOV DL,N2
MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H
END start
