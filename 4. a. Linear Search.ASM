.MODEL SMALL
.DATA
ARR_SIZE DB 0
ARR DB 10 DUP(?)
INPUT_SIZE DB 0DH,0AH,"ENTER THE SIZE OF THE ARRAY:$"
INPUT_ARRAY DB 0DH,0AH,"ENTER THE ELEMENTS OF THE ARRAY:$"
INPUT_ELEM DB 0DH,0AH,"ENTER THE ELEMENT YOU WISH TO SEARCH FOR:$"
ELEM DB 0
POS_ELEM DB 0DH,0AH, "ELEMENT FOUND AT POSITION:$"
NOT_FOUND DB 0DH,0AH,"ELEMENT NOT FOUND:$"
POS DW 0

.CODE
START:
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET INPUT_SIZE
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
SUB AL,30H
MOV ARR_SIZE,AL

MOV DX,OFFSET INPUT_ARRAY
MOV AH,09H
INT 21H

MOV CL,ARR_SIZE
MOV SI,0

ENTER_ARRAY_ELEMENTS:
        MOV AH,01H
        INT 21H
        SUB AL,30H
        MOV ARR[SI],AL
        INC SI
        MOV DL,20H
        MOV AH,02H
        INT 21H
        LOOP ENTER_ARRAY_ELEMENTS

MOV DX,OFFSET INPUT_ELEM
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
SUB AL,30H
MOV ELEM,AL

MOV CL,ARR_SIZE
MOV SI,0

LINEAR_SEARCH:
        MOV AL,ARR[SI]
        INC SI
        CMP AL,ELEM
        MOV POS,SI
        JZ FOUND
        LOOP LINEAR_SEARCH

MOV DX,OFFSET NOT_FOUND
MOV AH,09H
INT 21H

MOV AH,4CH
INT 21H

FOUND:
        MOV DX,OFFSET POS_ELEM
        MOV AH,09H
        INT 21H
        ADD POS,30H
        MOV DX,POS
        MOV AH,02H
        INT 21H

MOV AH,4CH
INT 21H

END START
